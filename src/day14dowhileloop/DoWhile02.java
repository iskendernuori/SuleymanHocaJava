package day14dowhileloop;

public class DoWhile02 {

	public static void main(String[] args) {
		// do-while ile while loop larinin farklari.
	
		
		
		
		int num=7;
		
		while(num<7) {
			System.out.println(num);// while da 7 girildiğnde hic bir sonuc vermez. cunku false dir.condiation yanlış olduğunda calışmaz.
			num++;
		}
		
		int num1=7;
		do {
			System.out.println(num1);// en az bir defa calısır. 
			num1++;
			
		}while(num1<7);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
