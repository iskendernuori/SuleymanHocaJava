package day02wrapperclasses;

public class Wrapper02 {

	public static void main(String[] args) {
		
		// int data tipinin max ve min dğerlerini bulup ekrana yazdırınız
		// int in wrapper class i Integer kullanmalıyoz
		System.out.println(Integer.MAX_VALUE);// bir class ın içindeki methodlara ulaşmak için class isminden sonra . koy listeden seçimm yap
		
		System.out.println(Integer.MIN_VALUE);
		
        // long data tipinin mx ve mn değerlerini yazdıralım
		// Wrapper  ====> çevreleyen kuşatan gibi. bayram şekerinin kabı bu manaya gelir
		
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		
		// tüm primitive lerin max. ve min. değerlerini wrapper class ları kullanarak yapın.Ödev
		
		
		System.out.println(Short.MAX_VALUE);
		System.out.println(Short.MIN_VALUE);
		
		System.out.println(Byte.MAX_VALUE);
		System.out.println(Byte.MIN_VALUE);
		
		System.out.println(Double.MAX_VALUE);
		System.out.println(Double.MIN_VALUE);
		
		System.out.println(Float.MAX_VALUE);
		System.out.println(Float.MIN_VALUE);
		
		System.out.println(Boolean.FALSE);
		
		
		
	}

}
