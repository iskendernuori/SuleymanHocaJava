package day05operators;

public class Increment04 {

	public static void main(String[] args) {
		
	// int data type inde bir tane variable oluşturun
		// bu variable i her seferinde 1 arttırmak için 3 yöntemini kullamın
		
		
		
		int num1 = 3;
		
		num1 = num1 + 1; // yöntem
		
		num1+=1; // kısa yol
		
		num1++;// en kısa yol
		
		System.out.println(num1);// nihayetinde sonuc 6 olur
		
		
		
		
		
	}

}
