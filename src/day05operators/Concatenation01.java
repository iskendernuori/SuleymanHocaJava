package day05operators;

public class Concatenation01 {

	public static void main(String[] args) {
		
		System.out.println("Ali" + "Can");// AliCan
		
		System.out.println("Ali " + "Can");// Ali Can
		
		System.out.println("Ali" + " Can"); // Ali Can
		
		System.out.println("Ali" + " " + "Can"); // Ali Can
		
		System.out.println(3 + "4"); // en az birisi string se concatination yapılıar
		
		
		System.out.println("3" + 4 + 5);
		
		System.out.println("3" + (4 + 5)); // parantezi toplayıp
		
		
		System.out.println(3 + 4 + "5" + 6);//756 java "" görünce string algılar. yani " öncesini int, sonrsında işlem varsa string algılar
		
		System.out.println(2*3 + "4" + 6/2);// 643
		
		System.out.println("2*3" + 4+ 5);//2*345 olur sonuç
		
		System.out.println("Learn " + "Java");
		
		
		
		
		
		
		
		
		
		
		

	}

}
