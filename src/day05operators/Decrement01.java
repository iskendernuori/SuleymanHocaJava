package day05operators;

public class Decrement01 {

	public static void main(String[] args) {
		
		// int data tipinde bir variable oluşturun.
		// variable in değerini 3 yöntemide kullanarak herdafasında 1 azaltın
		
		
		int num1 = 12;
		// uzun yol
		
		num1 = num1 -1;
		System.out.println(num1);//11
		
		num1-=1;
		System.out.println(num1);//10
		
		num1--;
		System.out.println(num1);//9  en kısa yol
		
		
		
		
		

	}

}
