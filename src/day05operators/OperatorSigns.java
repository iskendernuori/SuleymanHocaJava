package day05operators;

public class OperatorSigns {

	public static void main(String[] args) {
		
		int num1 = 12;
		int num2 = 20;
		
		System.out.println(num1==num2); // false  bizim için kontrol etti ve sonuç çıkardı  "==" bitişik olmalı
		//System.out.println(num1 == num2);
		
		System.out.println(12<20); // true 
		System.out.println(12!=20); // true 
		System.out.println(12>=20); // false
		
		System.out.println(12<=12); // true  // iki sonuctan birini alır. yani eşit veya küçük 
		
		boolean num3 = num1==num2;   // bunla diğer ikisi birlikte olur
		
		
		
		
		
		
		
		
		
	}

}
