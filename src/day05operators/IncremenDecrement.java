package day05operators;

public class IncremenDecrement {

	public static void main(String[] args) {
		
		
		// int data type de bir variable oluşturun.
		// Bu variable değerini çarpma ve bölme kullanarak
		// arttırın ve azaltın
		
		
		int num1 = 12;
		
		// uzun yolla çarpa kullanarak increment ypacağız
		
		num1 = num1 * 3;
		System.out.println(num1);//36

		num1*= 3;
		System.out.println(num1);//108
		
		//num1**; en kısa yol burada kullanılmaz. 
		
		
		num1 = num1 / 9;
		System.out.println(num1); //12
		
		num1/= 2;
		System.out.println(num1); // 6  çıkar. gene bu yöntemde en kısa yol kullanılmaz
		
		
 
		
		
		
		
		
		
	}

}
